#include<stdio.h>
int main()
{
    long long int t;
    scanf("%lld",&t);
    while(t--)
    {
        long long int n,tax,net;
        scanf("%lld",&n);
        if(n<=250000)
        {
            tax=0;
        }
        if(n>250000&&n<=500000)
        {
            tax=0.05*(n-250000);
        }
        if(n>500000&&n<=750000)
        {
            tax=12500+0.1*(n-500000);
        }
        if(n>750000&&n<=1000000)
        {
            tax=37500+0.15*(n-750000);
        }
        if(n>1000000&&n<=1250000)
        {
            tax=75000+0.2*(n-1000000);
        }
        if(n>1250000&&n<=1500000)
        {
            tax=125000+0.25*(n-1250000);
        }
        if(n>1500000)
        {
            tax=187500+0.3*(n-1500000);
        }
        net=n-tax;
        printf("%lld\n",net);
    }
     return(0);
}
